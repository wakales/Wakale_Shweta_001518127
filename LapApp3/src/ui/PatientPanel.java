    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        // TODO add your handling code here:
        String comm = txtAbCom.getText();
        int count =0;
        int group = 0;
        if(btnAge1.isSelected())
            group = 1;
        else if(btnAge2.isSelected())
            group = 2;
        else if(btnAge3.isSelected())
            group = 3;
        else
            JOptionPane.showMessageDialog(this, "Please select an age group");
        
            
        if(group == 1){
            for(Patient patient : patient.getPatient()) {
            if(comm.matches(patient.getCommunity()) && patient.getAge()<=18) 
                ++count;
            }
        }
        else if(group == 2){
            for(Patient patient : patient.getPatient()) {
            if(comm.matches(patient.getCommunity()) && patient.getAge()>18 && patient.getAge()<=60) 
                ++count;
            }
        }
                    
        txtAbnormal.setText(Integer.toString(count));
        btnAge1.setSelected(false);
        btnAge2.setSelected(false);
        btnAge3.setSelected(false);
    }//GEN-LAST:event_btnFilterActionPerformed


