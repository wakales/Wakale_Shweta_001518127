private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        model.setRowCount(0);
        
        
        for(Patient pi: patient.getPatient()){
           
            Object[] row= new Object[6];
            
            //row[0]=c;
            
            row[0]=pi;
            row[1]=pi.getAge();
            row[2]=pi.getCity();
            row[3]=pi.getMed();
            row[4]=pi.getCommunity();
            row[5]=pi.getBp();
            
            
            model.addRow(row);
            
        }
    }


