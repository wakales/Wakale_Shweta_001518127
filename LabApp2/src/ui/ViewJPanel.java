    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblCars.getModel();
        model.setRowCount(0);
        
        
        for(Car c: history.getHistory()){
           
            Object[] row= new Object[10];
            
            //row[0]=c;
            row[0]=c;
            row[1]=c.getAvail();
            row[2]=c.getYearofM();
            row[3]=c.getSeatnum();
            row[4]=c.getSerialnum();
            row[5]=c.getModelnum();
            row[6]=c.getCity();
            row[9]=c.getFleet();
            if(c.isCerti() == false) {
                row[7] = "Expired";
            } else {
                row[7] = "Active";
            }
            if(c.isUber() == false){
                row[8] = "NO";
            }
            else {
                row[8] = "YES";
            }
            
            
            model.addRow(row);
            
        }
    }

    
