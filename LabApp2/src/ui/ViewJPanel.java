    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCars.getSelectedRow();

            if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to update");
            }

            DefaultTableModel model = (DefaultTableModel) tblCars.getModel();
            String sno = model.getValueAt(selectedRowIndex, 4).toString();


            for(Car c : history.getHistory()) {
            if(c.getSerialnum().equals(sno)) {

            c.setAvail(txtAvailCar.getText());
            c.setFleet(Integer.parseInt(txtFleet.getText()));
            c.setCity(txtCity.getText());
//            Date date = new Date();
//            car.setTimeStamp(date);
            }
            }

            populateTable();
    }//GEN-LAST:event_btnUpdateActionPerformed


